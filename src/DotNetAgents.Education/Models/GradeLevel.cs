namespace DotNetAgents.Education.Models;

/// <summary>
/// Represents grade levels for K-12 education.
/// </summary>
public enum GradeLevel
{
    /// <summary>
    /// Kindergarten through 2nd grade (ages 5-8).
    /// </summary>
    K2,

    /// <summary>
    /// 3rd through 5th grade (ages 8-11).
    /// </summary>
    G3_5,

    /// <summary>
    /// 6th through 8th grade (ages 11-14).
    /// </summary>
    G6_8,

    /// <summary>
    /// 9th through 10th grade (ages 14-16).
    /// </summary>
    G9_10,

    /// <summary>
    /// 11th through 12th grade (ages 16-18).
    /// </summary>
    G11_12
}
